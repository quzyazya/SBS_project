# Ключ для хранения текущей цитаты 
QUOTE_DAILY_KEY = 'quote_of_the_day'
# Ключ в Redis для хранения индекса
QUOTE_INDEX_KEY = 'quote_index'

def get_quote_of_the_day():
    """Возврощает цитату дня (меняется раз в сутки)"""

    today = datetime.utcnow().strftime('%Y-%m-%d')
    daily_key = f'{QUOTE_DAILY_KEY}:{today}'

    # Проверяем, есть ли уже цитата на сегодня
    cached_quote = redis_client.get(daily_key)
    
    if cached_quote:
        # Если есть - возвращаем сохраненную
        return json.loads(cached_quote)
    
    # Если нет - берем следующую цитату по порядку
    current_index = redis_client.get(QUOTE_INDEX_KEY)

    if current_index is None:
        # Первый запуск: перемешиваем и начинаем с 0
        random.shuffle(QUOTES)
        current_index = 0
        redis_client.set(QUOTE_INDEX_KEY, current_index)
    else:
        current_index = int(current_index)

    # Берем цитату по индексу
    quote = QUOTES[current_index]

    # Увеличиваем индекс для следующего дня
    next_index = current_index + 1
    if next_index >= len(QUOTES):
        random.shuffle(QUOTES)
        next_index = 0

    redis_client.set(QUOTE_INDEX_KEY, next_index)

    # Сохраняем цитату на сегодня (срок жизни 24 часа)
    redis_client.setex(daily_key, int(timedelta(days=1).total_seconds()), json.dumps(quote))

    return quote

def reset_quotes():
    """Сброс цитат (для тестирования)"""
    random.shuffle(QUOTES)
    redis_client.set(QUOTE_INDEX_KEY, 0)
    # Очищаем все кэшированные цитаты
    for key in redis_client.scan_iter(f'{QUOTE_DAILY_KEY}:*'):
        redis_client.delete(key)